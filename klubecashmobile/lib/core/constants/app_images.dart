  /// Retorna o ícone baseado na categoria da loja
  static String getCategoryIcon(String category) {
    switch (category.toLowerCase()) {
      case 'alimentação':
      case 'food':
        return categoryFood;
      case 'moda':
      case 'fashion':
        return categoryFashion;
      case 'tecnologia':
      case 'tech':
        return categoryTech;
      case 'saúde':
      case 'health':
        return categoryHealth;
      case 'beleza':
