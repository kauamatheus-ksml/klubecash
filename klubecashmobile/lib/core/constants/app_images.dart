        return categoryHome;
      case 'serviços':
      case 'services':
        return categoryServices;
      default:
        return storesIcon;
    }
  }
  
  /// Retorna o ícone baseado no status da transação
  static String getStatusIcon(String status) {
    switch (status.toLowerCase()) {
      case 'aprovado':
      case 'approved':
        return successIcon;
      case 'pendente':
      case 'pending':
        return pendingIcon;
      case 'cancelado':
      case 'canceled':
        return cancelIcon;
      case 'erro':
      case 'error':
        return errorIcon;
      default:
        return infoIcon;
    }
  }
  
  /// Retorna a animação baseada no estado
  static String getStateAnimation(String state) {
    switch (state.toLowerCase()) {
      case 'loading':
        return loadingAnimation;
      case 'success':
        return successAnimation;
      case 'error':
        return errorAnimation;
      case 'empty':
        return emptyAnimation;
      case 'cashback':
        return cashbackAnimation;
      case 'money':
        return moneyAnimation;
      default:
        return loadingAnimation;
    }
  }
  
